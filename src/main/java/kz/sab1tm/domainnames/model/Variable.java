package kz.sab1tm.domainnames.model;

public class Variable {

    private String key;
    private String value;

    public Variable() {
    }

    public String getKey() {
        return key;
    }

    public void setKey(String key) {
        this.key = key;
    }

    public String getValue() {
        return value;
    }

    public void setValue(String value) {
        this.value = value;
    }
}
